----MySQL

CREATE TABLE POST_ITEM
(
    POST_NUM BIGINT NOT NULL,
    W_DATE DATETIME NOT NULL,
    TITLE VARCHAR(400) NOT NULL,
    URL VARCHAR(200) NOT NULL,
    NICKNAME VARCHAR(200) NOT NULL,
    USER_ID VARCHAR(200),
    USER_URL VARCHAR(200),
    PRIMARY KEY (POST_NUM)
);

CREATE TABLE POST
(
    POST_NUM BIGINT NOT NULL,
    CONTENT TEXT,
    PRIMARY KEY (POST_NUM)
);

CREATE TABLE ERROR_POST
(
    ERROR_NUM BIGINT AUTO_INCREMENT NOT NULL,
    POST_NUM BIGINT,
    ERROR_DESC TEXT,
    ERROR_DATE DATETIME NOT NULL,
    IS_RESOLVED INT DEFAULT 0,
    RESOLVED_DATE DATETIME,
    PRIMARY KEY (ERROR_NUM)
);

CREATE TABLE NAVER_POST_ITEM
(
    POST_NUM BIGINT NOT NULL,
    W_DATE DATETIME NOT NULL,
    TITLE VARCHAR(400) NOT NULL,
    CATEGORY INTEGER,
    URL VARCHAR(200) NOT NULL,
    NICKNAME VARCHAR(200) NOT NULL,
    MEM_LEVEL VARCHAR(20),
    MEM_LEVEL_ICON TEXT,
    PRICE VARCHAR(100),
    PRIMARY KEY (POST_NUM)
);

CREATE TABLE NAVER_POST
(
    POST_NUM BIGINT NOT NULL,
    CONTENT TEXT,
    PRIMARY KEY (POST_NUM)
);

CREATE TABLE FEEDBACK
(
    POST_NUM BIGINT AUTO_INCREMENT NOT NULL,
    TITLE VARCHAR(200),
    CONTENT TEXT,
    W_DATE DATETIME NOT NULL,
    IS_RESOLVED INT DEFAULT 0,
    RESOLVED_DATE DATETIME,
    PRIMARY KEY (POST_NUM)
);

CREATE TABLE LOGIN_COOKIES
(
    ID BIGINT AUTO_INCREMENT NOT NULL,
    COOKIE_DESC VARCHAR(200) NOT NULL,
    JSON_COOKIES TEXT NOT NULL,
    LOGIN_DATE DATETIME NOT NULL,
    PRIMARY KEY (ID)
);

CREATE TABLE VISITOR
(
    ID BIGINT AUTO_INCREMENT NOT NULL,
    VISIT_DATE DATETIME NOT NULL,
    IP_ADDRESS VARCHAR(200) NOT NULL,
    REQUEST_URI VARCHAR(200) NOT NULL,
    PRIMARY KEY (ID)
);


